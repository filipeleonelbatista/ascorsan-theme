<h2 id="instala-o">Instalação</h2>
<p>Como toda a instalação do <strong>Wordpress</strong>, você deve se atentar a alguns passos antes de configurar o tema e seu uso.</p>
<h3 id="configura-o-do-wordpress">Configuração do Wordpress</h3>
<h4 id="plugins">Plugins</h4>
<p>Lembre-se de baixar os Plugins necessários indicados na área de <strong>Requisitos</strong> e ativar eles, para seu correto funcionamento.</p>
<h4 id="nome-e-descri-o">Nome e Descrição</h4>
<p>Logo na instalação será necessário informar o nome do blog e uma descrição. Estas informações são utilizadas pelo tema para definição do rodapé e o titulo da pagina e também para o SEO. Caso não tenha ajustado isso no inicio, você pode ir na área administrativa do Wordpress, em configurações e, em geral, ira ter a opção para alterar o nome e a descrição. </p>
<h4 id="links">Links</h4>
<p>Caso não modifique nada você possa ter problemas com os links do Wordpress por isso será necessário ajustar. No Painel administrativo, no caminho <code>Configurações&gt;Links permanentes</code>, selecione a opção <code>Padrão</code> para que o site se comporte corretamente.</p>
<h4 id="posts-por-pagina">Posts por pagina</h4>
<p>As postagens por pagina, provavelmente não influenciarão neste tema mas caso ocorra algum problema, será necessário ajustar o valor da quantidade de postagens por página. No Painel administrativo, no caminho <code>Configurações&gt;Leitura</code>, No campo <code>As páginas do blog mostram no máximo</code> e coloque o valor <code>3</code>. O mesmo deve ser feito para o campo <code>Os feeds RSS mostram os últimos</code> inserindo o valor <code>3</code>.</p>
<h4 id="imagem-e-logotipo-do-tema">Imagem e logotipo do tema</h4>
<p>Por padrão não é inserido diretamente no tema a imagem, já o logotipo sim. no entanto ele pode ser alterado dentro do painel administrativo no caminho <code>Aparencia&gt;Personalizar&gt;Identidade do site</code> . Nesta área será possível subir a imagem da marca e o ícone do site. É recomendado que a logo tenha pelo menos <code>128x128px</code> de altura</p>
<h4 id="dados-de-contato">Dados de contato</h4>
<p>Por padrão não é inserido diretamente no tema as informações de contato. No entanto se não houver nada, será informado as informações atuais da empresa. Se for necessário alterar, basta entrar no painel administrativo no caminho <code>Aparencia&gt;Personalizar&gt;Dados de contato</code> Para informar os dados da empresa. Lembrando que estes dados são utilizados em mais locais do site como, por exemplo, Página de contato, Rodapé, Redes sociais, etc.</p>
